(async () => {
  const TEST_NAME = "Hide node modules with user role";

  // log in
  const { page } = await logIn({ userId: 10 });
  await assertText(page, "Library");
  
  // go to recording "Replay with node module logs"
  await page.click("text=Replay with node module logs");
  
  // assert recording loaded
  await expect(page.locator("#video")).toBeVisible();
  
  // go to DevTools
  await page.click("text=ViewerDevTools");
  
  // assert DevTools loaded
  await assertText(page, "Console");
  
  // clear console and open node module logs
  await page.click('[data-test-id="ConsoleMenuToggleButton"]');
  if (!await page.isChecked("#FilterToggle-nodeModules")) {
    await page.click("#FilterToggle-nodeModules");
  }
  await page.click("#FilterToggle-logs");
  
  // view node module logs
  await page.waitForTimeout(5000);
  const message = page.locator('[data-test-name="Message"]');
  await expect(message).toHaveCount(1, { timeout: 2 * 60 * 1000 });
  
  // hide node modules
  await page.click("#FilterToggle-nodeModules");
  
  // assert node module logs hidden
  await expect(message).toHaveCount(0);
  

  process.exit();
})();