const { assert,assertElement,assertText,expect,faker,getInbox,getValue,launch,assertNotElement,assertNotText,buildUrl,deleteTeam,getBoundingClientRect,getPlaybarTooltipValue,logIn,logInToFacebook,parseInviteUrl,setFocus,waitForFrameNavigated } = require("./helpers");

(async () => {
  // log in
  const { page } = await logIn({ userId: 10 });
  await assertText(page, 'Library');
  
  // go to recording
  await page.click('[href="/recording/log-in-and-out-pr-deployment-replay-qa-wolf--924c1067-3cff-43fd-ad35-39a1e350c5f1"]');
  
  // assert recording loaded
  await assertText(page, 'Log in and out (PR deployment)');
  await assertText(page, 'DevTools');
  
  // go to DevTools
  await page.click("text=ViewerDevTools");
  
  // assert DevTools loaded
  await assertText(page, 'Console');
  
  // clear console
  // check if console-filter-toggle is already open
  if (await page.isChecked("#hide-node-modules")) {
    await page.click("#hide-node-modules");
  };
  await page.click("#show-errors");
  await page.waitForTimeout(5000);
  const messages = page.locator('[title="Log"]');
  await page.waitForTimeout(5000);
  await expect(messages).toHaveCount(25); //was 16 before
  
  // hide node modules
  await page.click("#hide-node-modules");
  
  // assert messages hid
  await expect(messages).toHaveCount(0);

  process.exit();
})();